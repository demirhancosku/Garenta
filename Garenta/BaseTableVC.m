{
    self = [super initWithStyle:style];
    if (self) {
        // Custom initialization
    }
    
    return self;
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    [[self tableView] setSeparatorColor:[ApplicationProperties getMenuTableBackgorund]];
    [[self tableView] setBackgroundColor:[ApplicationProperties getMenuTableBackgorund]];
    [[self tableView] setSeparatorInset:UIEdgeInsetsZero];
    [[self tableView] setRowHeight:65.0];
}

