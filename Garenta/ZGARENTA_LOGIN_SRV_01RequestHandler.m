- (void)updateLoginServiceCompleted:(id <Requesting>)request
{
    NSMutableDictionary *userInfoDict = [NSMutableDictionary dictionaryWithDictionary:request.userInfo];
    NSData *responseData = request.responseData;
    if ([responseData length] > 0) {
        NSError *error;
        LoginService *item = [LoginService parseLoginServiceEntryWithData:responseData error:&error];
        if (error) {
        	userInfoDict[kParsingError] = error;
        }
        else{
        	userInfoDict[kResponseItem] = item;
        }
    }
    [[NSNotificationCenter defaultCenter] postNotificationName:kUpdateLoginServiceCompletedNotification object:self userInfo:userInfoDict];

}

- (void)updateLoginService:(LoginService *)aLoginService
{
	[self loadCSRFData];
    NSError *error = nil;
    NSString *payload = nil;
    if (self.useJSON) {
        payload = [service getJSONForUpdateRequest:aLoginService error:&error];
    }
    else {
        payload = [service getXMLForUpdateRequest:aLoginService error:&error];
    }
    
    if (error) {
        NSMutableDictionary *userInfoDict = [@{} mutableCopy];
        userInfoDict[kParsingError] = error;
        [[NSNotificationCenter defaultCenter] postNotificationName:kUpdateLoginServiceCompletedNotification object:self userInfo:userInfoDict];
    }
    else {
        ODataQuery *query = [[ODataQuery alloc] initWithURL:aLoginService.baseUrl];
    	NSDictionary *userInfoDict = @{kResponseParentItem : aLoginService , kRequestUserInfoCompletedNotification : kUpdateLoginServiceCompletedNotification};
    	[connectivityHelper executeUpdateAsyncRequestWithQuery:query andBody:payload andCSRFData:csrfData andRequestDelegate:self andDidFinishSelector:@selector(updateLoginServiceCompleted:) andUserInfo:userInfoDict andEtag:aLoginService.etag];
    }
}

